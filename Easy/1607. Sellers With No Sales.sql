WITH TEMP AS
(
   SELECT DISTINCT SELLER_ID
   FROM ORDERS
   WHERE SALE_DATE >= '2020-01-01' AND SALE_DATE <= '2020-12-31'
) 

SELECT SELLER_NAME
FROM SELLER 
WHERE SELLER_ID NOT IN (SELECT * FROM TEMP)
ORDER BY 1;