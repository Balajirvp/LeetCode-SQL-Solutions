SELECT DISTINCT SELLER_NAME
FROM SELLER
WHERE SELLER_ID NOT IN
(
    SELECT DISTINCT SELLER_ID 
    FROM ORDERS
    WHERE SALE_DATE >= '2020-01-01' AND SALE_DATE <= '2020-12-31'
)
ORDER BY 1;   

