SELECT DISTINCT NUM AS CONSECUTIVENUMS
FROM
(
    SELECT NUM, LEAD(NUM) OVER(ORDER BY ID) AS NEXT_NUM,
                LAG(NUM) OVER(ORDER BY ID) AS PREV_NUM
    FROM LOGS
) A
WHERE NUM = PREV_NUM AND NUM = NEXT_NUM;