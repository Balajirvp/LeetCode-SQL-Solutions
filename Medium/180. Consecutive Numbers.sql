SELECT DISTINCT A.NUM AS CONSECUTIVENUMS
FROM LOGS A
INNER JOIN LOGS B
ON A.ID = B.ID + 1 AND A.NUM = B.NUM
INNER JOIN LOGS C
ON A.ID = C.ID - 1 AND A.NUM = C.NUM;

